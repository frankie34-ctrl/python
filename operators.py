#Arithmetic operators
x = 24
y = 32

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y) #Modulus-it returns remainder after division

# Comparison Operators
print(x<y)
print(x>y)
print(x==y)
print(x!=y)
print(x>=y)
print(x<=y)

#Assignment Operators
a = 10
print(a)

a += 2
print(a)
a -= 2
print(a)
a *= 2
print(a)
a /= 2
print(a)

#logical operators - and, or, not
print(34 < 50 and 56>200)
print(not(34 < 50 or 56>200))
print(34 < 50 or 56>200)

