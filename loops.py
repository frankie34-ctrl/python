# While loop

number = 5
while number <= 10:
    print(number)
    number += 1

# Decrement
number = 30
while number >= 25:
    print(number)
    number -= 1

#For loop
for x in range(105,110):
    print("Number is",x)

devices = ["laptop", "mouse", "computer"]
for device in devices:
    print(device)


